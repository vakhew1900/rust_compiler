fn main() {
    let ch = 'x';
    println!("char is {}", ch);

    let integ = 1000000000;
    println!("int is {}", integ);
    println!("int is {}", 100);

    let f = 1.2;
    println!("float is {}", f);
    println!("float is {}", 1e10);

    let bool_val = true;
    let b1 = !bool_val;
    println!("boolean is {}", bool_val);
    println!("boolean is {}", b1);
}